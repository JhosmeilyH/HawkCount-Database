
DROP DATABASE IF EXISTS hk;
CREATE DATABASE hk;


USE hk;

CREATE TABLE Species (
    SpeciesID 	INT 	PRIMARY KEY ,
    SpecieName VARCHAR(100)  NULL,
    SpecieAbbreviation VARCHAR(10) NULL
);

CREATE TABLE Locations (
	LocationID INT PRIMARY KEY,
	Address VARCHAR(255),
    City  VARCHAR(100) NOT NULL,
    State VARCHAR(50) NOT NULL,
    Zip   VARCHAR(20) NOT NULL
);
CREATE TABLE Observations (
	ObservationID INT PRIMARY KEY,
    ObservationStartTime TIME NULL,
	ObservationEndTime TIME NULL,
    SpeciesTotal INT NULL
);
CREATE TABLE ObservationTimeRange (
	ObservationTimeRangeID INT,
    ObservationID INT,
    ObservationTimeRangeStart TIME NULL,
    ObservationTimeRangeEnd TIME NULL,
    ObservationTimeRangeSpeciesTotal INT,
	PRIMARY KEY (ObservationTimeRangeID, ObservationID),
    FOREIGN KEY (ObservationID) REFERENCES Observations(ObservationID)
);

CREATE TABLE ObservationTimeRangeSpecies (
	ObservationTimeRangeID INT,
    SpeciesID INT,
	SpecieAbbreviation VARCHAR(10) NULL,
    SpecieCount INT NULL,
    PRIMARY KEY (ObservationTimeRangeID, SpeciesID),
    FOREIGN KEY (ObservationTimeRangeID) REFERENCES ObservationTimeRange(ObservationTimeRangeID)
);
CREATE TABLE ObservationConditions (
    ObservationID INT,
    ObservationTimeRangeID INT,
    WindScale INT NULL,
    WindSpeed DECIMAL(5, 2) NULL,
    WindDirection VARCHAR(15) NULL,
    VisibilityDistance DECIMAL(6, 2) NULL,
    CloudPercentage DECIMAL(5, 2) NULL,
    Temperature FLOAT NULL,
    PrecipitationPresent VARCHAR(3) NULL,
    PrecipitationType VARCHAR(50) NULL,
    HumidityLevel DECIMAL(6, 2) NULL,
    AirPressure VARCHAR(50) NULL,
    FlightDirection VARCHAR(255) NULL,
    FlightHeight INT NULL,
    FlightDescription VARCHAR(80) NULL,
	PRIMARY KEY (ObservationID, ObservationTimeRangeID),
    FOREIGN KEY (ObservationID) REFERENCES Observations(ObservationID),
    FOREIGN KEY (ObservationTimeRangeID) REFERENCES ObservationTimeRange(ObservationTimeRangeID)
);
CREATE TABLE NovemberObservationMetrics (
	MetricsID INT PRIMARY KEY,
    LocationID INT ,
    ObservationMonthTotalHrs DECIMAL(5, 2),
    ObservationMonthTotalSpecies INT,
    FOREIGN KEY (LocationID) REFERENCES Locations(LocationID)
);

