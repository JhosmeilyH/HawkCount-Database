SELECT s.SpecieName,COALESCE(SUM(ots.SpecieCount), 0) AS TotalSpeciesObserved
FROM Species s
LEFT JOIN ObservationTimeRangeSpecies ots ON s.SpeciesID = ots.SpeciesID
GROUP BY s.SpecieName
WITH ROLLUP;

SELECT s.SpecieName AS MostFrequentBird, SUM(ots.SpecieCount) AS Totalobservations
FROM NovemberObservationMetrics nom
JOIN ObservationTimeRange otr ON nom.LocationID = otr.ObservationID
JOIN ObservationTimeRangeSpecies ots ON otr.ObservationTimeRangeID = ots.ObservationTimeRangeID
JOIN Species s ON ots.SpeciesID = s.SpeciesID
GROUP BY s.SpecieName
ORDER BY Totalobservations DESC
LIMIT 1;


